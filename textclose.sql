     , INDEX (USER_ID)
     , CONSTRAINT FK_USER_ADDRESS_1 FOREIGN KEY (USER_ID)
                  REFERENCES TEXTCLOSE.USER (USER_ID)
);

CREATE TABLE TEXTCLOSE.USERCREDITCARD (
