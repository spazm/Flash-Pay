DROP TABLE IF EXISTS TEXTCLOSE.USER_CONTACT;
DROP TABLE IF EXISTS TEXTCLOSE.USER_SETTING;
DROP TABLE IF EXISTS TEXTCLOSE.USER_TAB;
DROP TABLE IF EXISTS TEXTCLOSE.BUSINESSCONTACT;
DROP TABLE IF EXISTS TEXTCLOSE.BUSINESSADDRESS;
DROP TABLE IF EXISTS TEXTCLOSE.USERCREDITCARD;
DROP TABLE IF EXISTS TEXTCLOSE.USER_ADDRESS;
DROP TABLE IF EXISTS TEXTCLOSE.USER_ACTIVITY;
DROP TABLE IF EXISTS TEXTCLOSE.USER;
DROP TABLE IF EXISTS TEXTCLOSE.NOTIFICATION_LOG;
DROP TABLE IF EXISTS TEXTCLOSE.BUSINESS;

CREATE TABLE TEXTCLOSE.BUSINESS (
       BUSINESS_ID BIGINT NOT NULL AUTO_INCREMENT
     , NAME VARCHAR(100) NOT NULL
     , USERNAME VARCHAR(50) NOT NULL
     , PASSWORD VARCHAR(20) NOT NULL
     , SIGNUP_DATE TIMESTAMP NOT NULL
     , ACTIVE BOOLEAN NOT NULL
     , DEACTIVE_DATE TIMESTAMP
     , ACCOUNT_NUMBER VARCHAR(10) NOT NULL
     , PRIMARY KEY (BUSINESS_ID)
);

CREATE TABLE TEXTCLOSE.NOTIFICATION_LOG (
       NOTIFICATION_LOG_ID BIGINT NOT NULL AUTO_INCREMENT
     , TYPE INTEGER NOT NULL
     , NUMBER VARCHAR(20) NOT NULL
     , NOTIFICATION VARCHAR(200) NOT NULL
     , NOTIFICATION_BY INTEGER NOT NULL
     , NOTIFICATION_TIME TIMESTAMP NOT NULL
     , PRIMARY KEY (NOTIFICATION_LOG_ID)
);

CREATE TABLE TEXTCLOSE.USER (
       USER_ID BIGINT NOT NULL AUTO_INCREMENT
     , USERNAME VARCHAR(100) NOT NULL
     , FIRSTNAME VARCHAR(50) NOT NULL
     , LASTNAME VARCHAR(50) NOT NULL
     , EMAIL VARCHAR(100) NOT NULL
     , PASSWORD VARCHAR(50) NOT NULL
     , PRIMARY KEY (USER_ID)
);

CREATE TABLE TEXTCLOSE.USER_ACTIVITY (
       USER_ACTIVITY_ID BIGINT NOT NULL AUTO_INCREMENT
     , CHECKIN_TIME TIMESTAMP NOT NULL
     , CHECKOUT_TIME TIMESTAMP
     , USER_ID BIGINT NOT NULL
     , BUSINESS_ID BIGINT NOT NULL
     , CHECKOUT_BY INTEGER
     , PRIMARY KEY (USER_ACTIVITY_ID)
     , INDEX (USER_ID)
     , CONSTRAINT FK_USER_ACTIVITY_1 FOREIGN KEY (USER_ID)
                  REFERENCES TEXTCLOSE.USER (USER_ID)
     , INDEX (BUSINESS_ID)
     , CONSTRAINT FK_USER_ACTIVITY_2 FOREIGN KEY (BUSINESS_ID)
                  REFERENCES TEXTCLOSE.BUSINESS (BUSINESS_ID)
);

CREATE TABLE TEXTCLOSE.USER_ADDRESS (
       USER_ADDRESS_ID BIGINT NOT NULL AUTO_INCREMENT
